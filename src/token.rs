#[derive(Clone, Debug, PartialEq)]
pub enum Token {
    Empty,
    Indent(usize),
    Dedent(usize),
    NewLine,
    None,
    True,
    False,
    Plus,
    Minus,
    Star,
    Slash,
    Bang,
    BangEqual,
    Equal,
    EqualEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    Colon,
    Comma,
    Dot,
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    LeftBrace,
    RightBrace,
    And,
    Or,
    If,
    Elif,
    Else,
    Def,
    For,
    While,
    In,
    Continue,
    Break,
    Return,
    Numeric(f64),
    Identifier(String),
    Eof,
    Error(String),
}
